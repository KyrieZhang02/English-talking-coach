-- The trigger (security definer) handles INSERT; no client INSERT policy needed.

create policy "users: select own"
  on public.users for select
  using (auth.uid() = id);

create policy "users: update own"
  on public.users for update
  using (auth.uid() = id)
  with check (auth.uid() = id);

-- ----------------------------------------------------------------
-- scenes
-- ----------------------------------------------------------------

-- Any visitor (including anon) can browse system scenes (landing page use-case)
create policy "scenes: anyone reads system scenes"
  on public.scenes for select
  using (is_system = true);

-- Authenticated users can also read their own custom scenes
create policy "scenes: owner reads own custom scenes"
  on public.scenes for select
  to authenticated
  using (auth.uid() = created_by);

-- Authenticated users may create custom (non-system) scenes
create policy "scenes: owner inserts custom scenes"
  on public.scenes for insert
  to authenticated
  with check (
    auth.uid() = created_by
    and is_system = false
  );

create policy "scenes: owner updates own custom scenes"
  on public.scenes for update
  to authenticated
  using  (auth.uid() = created_by and is_system = false)
  with check (auth.uid() = created_by and is_system = false);

create policy "scenes: owner deletes own custom scenes"
  on public.scenes for delete
  to authenticated
  using (auth.uid() = created_by and is_system = false);

