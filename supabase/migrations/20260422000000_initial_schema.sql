-- ----------------------------------------------------------------
-- 1. users
--    Profile table that extends auth.users (1-to-1).
--    Created automatically via trigger on first sign-up.
-- ----------------------------------------------------------------

create table public.users (
  id            uuid        primary key references auth.users(id) on delete cascade,
  email         text        not null,
  display_name  text,
  avatar_url    text,
  created_at    timestamptz not null default now(),
  updated_at    timestamptz not null default now()
);

comment on table public.users is 'Public user profiles extending auth.users.';

-- ----------------------------------------------------------------
-- 2. scenes
--    Preset and user-created conversation scenarios.
--    is_system = true rows are seeded by migrations only.
-- ----------------------------------------------------------------

create table public.scenes (
  id               uuid             primary key default gen_random_uuid(),
  name             text             not null,
  description      text             not null,
  -- Full system prompt for the AI role in this scenario
  ai_role_prompt   text             not null,
  difficulty       difficulty_level not null,
  -- e.g. ARRAY['negotiation', 'formal vocabulary']
  training_focus   text[]           not null default '{}',
  is_system        boolean          not null default false,
  -- null for system scenes; set to user id for custom scenes
  created_by       uuid             references public.users(id) on delete set null,
  created_at       timestamptz      not null default now()
);

comment on table  public.scenes is 'Preset and user-custom conversation scenarios.';
comment on column public.scenes.ai_role_prompt is 'System prompt injected as the AI persona for this scene.';

-- ----------------------------------------------------------------
-- 3. sessions
--    One row per conversation attempt.
--    transcript stores the full message history as a JSONB array:
--      [{ "role": "user"|"assistant", "content": "...", "ts": "<iso>" }]
-- ----------------------------------------------------------------

create table public.sessions (
  id                       uuid           primary key default gen_random_uuid(),
  user_id                  uuid           not null references public.users(id) on delete cascade,
  -- null when using a fully custom (ad-hoc) scene
  scene_id                 uuid           references public.scenes(id) on delete set null,
  -- filled only when scene_id is null (user-described custom scenario)
  custom_scene_description text,
  status                   session_status not null default 'in_progress',
  -- populated when session ends
  duration_seconds         integer        check (duration_seconds >= 0),
  transcript               jsonb          not null default '[]'::jsonb,
  started_at               timestamptz    not null default now(),
  ended_at                 timestamptz,
  created_at               timestamptz    not null default now(),

  constraint session_has_scene check (
    scene_id is not null or custom_scene_description is not null
  )
);

comment on table  public.sessions   is 'One record per voice conversation attempt.';
comment on column public.sessions.transcript is
  'Array of {role, content, ts} objects. Appended in real-time during the session.';

-- ----------------------------------------------------------------
-- 4. reports
--    One-to-one with sessions; generated by the backend after
--    a session completes.
--
--    pronunciation_feedback JSONB shape:
--      [{ "phoneme": "th", "count": 3, "examples": ["the", "that"] }]
--
--    grammar_errors JSONB shape:
--      [{ "original": "I have went", "correction": "I have gone",
--         "rule": "past participle", "severity": "high"|"medium"|"low" }]
-- ----------------------------------------------------------------

create table public.reports (
  id                     uuid        primary key default gen_random_uuid(),
  session_id             uuid        not null unique references public.sessions(id) on delete cascade,
  user_id                uuid        not null references public.users(id)   on delete cascade,

  -- Component scores (0–100)
  pronunciation_score    numeric(5,2) check (pronunciation_score    between 0 and 100),
  grammar_score          numeric(5,2) check (grammar_score          between 0 and 100),
  fluency_score          numeric(5,2) check (fluency_score          between 0 and 100),
  vocabulary_score       numeric(5,2) check (vocabulary_score       between 0 and 100),
  performance_score      numeric(5,2) check (performance_score      between 0 and 100),
  -- Weighted aggregate of the above
  overall_score          numeric(5,2) check (overall_score          between 0 and 100),

  pronunciation_feedback jsonb        not null default '[]'::jsonb,
  grammar_errors         jsonb        not null default '[]'::jsonb,

  strengths              text[]       not null default '{}',
  suggestions            text[]       not null default '{}',

  created_at             timestamptz  not null default now()
);

comment on table public.reports is 'AI-generated analysis report for a completed session.';

-- ----------------------------------------------------------------
-- 5. weakness_profile
--    One row per user; upserted by the backend after each report.
--
--    pronunciation_weaknesses JSONB shape:
--      [{ "phoneme": "th", "error_count": 12,
--         "examples": ["this","think"], "last_seen": "<iso>" }]
--
--    grammar_weaknesses JSONB shape:
--      [{ "pattern": "present perfect vs simple past", "error_count": 7,
--         "examples": ["I have went"], "last_seen": "<iso>" }]
--
--    vocabulary_gaps JSONB shape:
--      [{ "category": "business idioms", "gap_count": 4,
--         "suggested_words": ["leverage","synergy"], "last_seen": "<iso>" }]
-- ----------------------------------------------------------------

create table public.weakness_profile (
  id                       uuid        primary key default gen_random_uuid(),
  user_id                  uuid        not null unique references public.users(id) on delete cascade,
  pronunciation_weaknesses jsonb       not null default '[]'::jsonb,
  grammar_weaknesses       jsonb       not null default '[]'::jsonb,
  vocabulary_gaps          jsonb       not null default '[]'::jsonb,
  total_sessions           integer     not null default 0 check (total_sessions >= 0),
  updated_at               timestamptz not null default now()
);

comment on table public.weakness_profile is
  'Aggregated weakness profile for a user, rebuilt after each session report.';

