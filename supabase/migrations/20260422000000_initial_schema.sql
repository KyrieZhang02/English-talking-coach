-- ----------------------------------------------------------------
-- Reusable updated_at stamper
-- ----------------------------------------------------------------

create or replace function public.handle_updated_at()
returns trigger
language plpgsql
as $$
begin
  new.updated_at = now();
  return new;
end;
$$;

create trigger trg_users_updated_at
  before update on public.users
  for each row execute function public.handle_updated_at();

create trigger trg_weakness_profile_updated_at
  before update on public.weakness_profile
  for each row execute function public.handle_updated_at();

-- ----------------------------------------------------------------
-- Auto-provision user profile + weakness_profile on first sign-up.
-- security definer so the trigger can bypass RLS.
-- ----------------------------------------------------------------

create or replace function public.handle_new_user()
returns trigger
language plpgsql
security definer
set search_path = public
as $$
begin
  insert into public.users (id, email)
  values (new.id, new.email)
  on conflict (id) do nothing;

  insert into public.weakness_profile (user_id)
  values (new.id)
  on conflict (user_id) do nothing;

  return new;
end;
$$;

create trigger trg_on_auth_user_created
  after insert on auth.users
  for each row execute function public.handle_new_user();

